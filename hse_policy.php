        	<h3 class="reduce">AMSLEY TECHNOLOGIES NIGERIA LIMITED H.S.E POLICY STATEMENT</h3>
            <p>It is the policies of AMSLEY TECHNOLOGIES NIGERIA LIMITED conduct its business in a socially responsible and ethical manner ensuring safety, health and a safe environment.</p>
            <p>To this end, it is the company to survive in all her activities not to endanger the health, safety, environment and security of its employees and other persons we may come in contact within the course of its operations.</p>
            <p>To achieve this objective, the company observed all laid down health, safety, environment and security instructions of our clients and statutory on safety of Federal Government of Nigeria.</p>
            <p>In view of the nature of work in the industry, the company sets out certain rules and regulations to guide her employee in health and safety of the employee’s safe operation of equipment and the protect	ion of neighboring environment.</p>
            <ul>
            <li>•	It is our HSE policy that work cannot be carried in any work/delivery site or installation without undertaking a familiarization tour of the site to find out the potential hazard associated with the job.</li>
            <li>•	This policy tries to allocate safety procedure to individual and offices. All staff, new employees and all third parties are entitle to a copy of the HSE policy and manual.</li>
            <li>•	Cardinal to our HSE policy is that, injury to personnel accident damages to equipment and environment is not acceptable.</li>
            <li>•	Training program on HSE course is mandatory to employee’s to update their knowledge on their various portfolios.</li>
            
            </ul>
            
            <p>Management accepts its legal and social obligations and conferred on it’s as an employer of labour and all various portfolios.</p>
            <p>However, it is imperative to warn that all negative inclinations to safety management shall be frowned upon and discouraged, while positive attitudes shall be rewarded.</p>
            <h4 class="reduce">SAFETY OFFICER</h4>
            
            <ul>
            <li>The safety officer is preferably an OND level certificate holder in Health Science or any Engineering field with safety training.</li>
            <li>The safety officer shall work in accordance with safety policy to achieve goals in safety.</li>
            <li>Monitor compliance with procedure for safe practices.</li>
            <li>Develop inspection charts and checklists for the supervisors.</li>
           
            </ul>
            <h4 class="reduce" >ADMIN. MANAGER AND ACCOUNTANT</h4>
            
            <ul>
