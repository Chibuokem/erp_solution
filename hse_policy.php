            <li>o	Identify the need for and arrange safety training roe categories or staff in liaison with department heads and the line supervisors.</li>
            <li>Organize and conduct safety induction for all employees.</li>
            <li>Organize regular safety audits, drills and meeting.</li>
            <li>Attend the safety meeting as set out in company management structure.</li>
            <li>Make requisition for all safety personal protective equipment and life saving appliances.</li>
            <li>Liaise with Project Engineer and Sales Manager to establish safety Training needs.</li>
            <li>Organize formal safety training for all employees as may be necessary</li>
            
            </ul>
            
            <h4 class="reduce">RESPONSIBILITIES OF EMPLOYEES</h4>
            <ul>
            	<li>Employees in this respect shall mean all other AMSLEY TECHNOLOGIES NIGERIA LIMITED workers and all Sub-contractors employees working directly for or through third parties for the company.</li>
                <li>They shall read and be guided by the content of the company’s Safety manual.</li>
                <li>o	Employees shall correct or report orally or in writing to their supervisor and safe condition or act observed.</li>
                <li>o	Safety being in the interest of all, employees shall conform to safety directives emanating from Management and Supervisors as far as safety concern.</li>
                <li>Every employee shall be accountable for his/her action where necessary.</li>
                <li>Employees must report to their Supervisors any accident or injuries.</li>
                <li>o	Employees shall notify their Supervisor when taking medication that could affect their job performance.</li>
                <li>The idea of collective responsibility is binding on all employees.</li>
                <li>Also employees’ failure to attend safety Drills and Training without permission shall be viewed seriously.</li>
                  </ul>
                
                <h4 class="reduce">USE OF PPE</h4>
                <p>The use of All Personal Protective Equipment (PPE) is mandatory to all workers engaged in the Company’s operation that require such P.P.E. No employee shall flout the wearing of his/her P.P.E and if anyone is found he/she shall be severely cautioned and in extreme cases the employee shall be disciplined.</p>
                <p>The project Manager of Engineer, Supervisor and safety Officer are charge with enforcement of the rule.</p>
                <h4 class="reduce">PERSONAL PROTECTION EQUIPMENT (PPE)</h4>
                <p>The personal protection Equipment shall include:</p>
                <h4 class="reduce">WORK CLOTHES</h4>
                <p>Field Work protective clothing suitable to the operational demand and as specified by client should always be warned on work site. I.e. cover all.</p>
                <p>The company shall provide various types of safety Head gear, caps and Helmets available for protecting workers from impact and penetration from falling and flying objects and from Limited electric shock and burn.</p>
                <h4 class="reduce">EYE PROTECTION</h4>
                <p>Safety spectacles, Goggles and face shield are provided for protection against variety of hazard flying particles, splashes of corrosive liquid duct and harmful radiation. This is mandatory to staff and visitors.</p>
                <h4 class="reduce">EAR PROTECTION</h4>
                <p>The ear is protected against the harmful effect of noise by means of ear plugs and muffs.</p>
				<h4  class="reduce">HAND AND FINGER PROTECTION</h4>      
                <p>Due to the effect of Machineries and Tools on site the hands and fingers are exposed to cuts, scratch, bruises and burn. For this reason employees on the site shall be given Gloves to suit the specification of job.</p>          
                <h4  class="reduce">CARE OF PPE</h4>
                <p>The company shall issue PPE to employees must maintain PPE’s in serviceable state. PPE’s damaged accidentally or lost shall be replaced unconditionally. However, cases of willful damage or neglect shall be viewed seriously; the supervisor must impress it upon workers to handle P.P.E’s safety.</p>
                <h4  class="reduce">LIFTING OPERATION</h4>
                <p>About a quarter of the occupational injuries result from material handling i.e., lifting from one place to another, etc. these injuries can occur at any time during any phase of operations. The most common is sprain, strain fractures and so on.</p>
                
                <h4  class="reduce">Before lifting of equipments is carried out these rules for lifting is considered:</h4>
                <ul>
                <li>Weight of the load is should checked before lifting, if necessary it should be held before lifting.</li>
                <li>If weight of load is not certain request for help.</li>
                <li>Check it for splinter, grease to avoid it slipping off.</li>
                <li>Ensure gloves are worn for firm grip.</li>
                <li>Proceed to lift by planting your feet firmly, one foot slightly forward of the other.</li>
                <li>Tick in your chin, back straight.</li>
                <li>Bend your knees not your back.</li>
                <li>Lift smoothly don’t jerk.</li>
                <li>Keep load close to your back.</li>
                <li>Set down gentle don’t throw.</li>
                <li>Don’t obstruct your forward view with load.</li>
               
                </ul>
                <h4  class="reduce">MECHANICAL LIFTING</h4>
                <p>Lifting heavy loads with cranes, chain block and Tackles can be hazardous if done correctly.</p>
                <h4  class="reduce">FIRST AID</h4>
                <p>First Aid can make the difference in life and death in the event of an accident. The administering of First Aid treatment to a patient pending the attention of a Doctor is therefore of paramount importance.
The company shall equip First Aid boxes for all offices, vehicles, workshop and sites. Also some key staff shall be trained to administer First Aid treatment to an injured employee as directed or advised by the company Doctor.
</p>
          <h4  class="reduce">KISS OF LIFE (To Restore Breath)</h4>
          <ol>
          	<li>Turn person on his/her back</li>
            <li>Life head and put folded towel neck.</li>
            <li>Pull lower jaw forward and remove any foreign object.</li>
            <li>Hold his mouth open, pinch his nose closed with finger thumb</li>
            <li>Cover his mouth with yours and blow until victim’s chest fills</li>
            <li>Let it empty, keep doing this (not more than 20 times a minute)</li>
            <li>On an adult, give blows to the chest to try to start his heart heat.</li>
          </ol>
          <h4  class="reduce">HEART MESSAGE</h4>
          <p>Please lower half bone, press about once every second with moderate pressure. (Less for babies).</p>
          <h4 class="reduce">ELECTRIC SHOCK</h4>
          <ol>
          	<li>In this case, first of all switch off electricity</li>
            <li>Do not touch victim if he is holding anything electrical.</li>
            <li>Push victim away with wooden material.</li>
            <li>If victim is unconscious but breathing, turn him or her side or front.</li>
            <li>Turn head to side and back so he can’t choke</li>
            <li>Keep watch until doctor arrives.</li>
           
          </ol>
          <h4  class="reduce">SEVERE BLEEDING</h4>
          <ol>
          	<li>Cover the wound with your hand and press firmly.</li>
            <li>Get a cloth that is clean to cover the wound and press hard.</li>
            <li>If possible, raise the wound above the chest or breast bone, get him to sit.</li>
            <li>If unconscious but still breathing try to stop bleeding by applying the above methods. Possibly, put in recovery position and watch closely that he/her does not choke.</li>
            <li>If not breathing, give “KISS OF LIFE”. Try getting help from more hands to stop bleeding at the same time.</li>
          </ol>
            <h4 class="reduce">SCALDS AND BURNS</h4>
            <ol>
            <li>Immediately put burn under cold water or cold water over it for at least 20 minutes, remove clothing if not struck to skin.</li>
            <li>IF NOT BREATHING, GIVE “KISS OF LIFE”.</li>
            <li>After cooling cover with non-fluffy cloth, cotton/linen, sheet/pillow case.</li>
            <li>If unconscious but still put in recovery position and watch closely.</li>
            <li>Do not pick blisters.</li>
            
            </ol>
